from collections import deque


class Solution:
    def updateMatrix(self, mat):
        INF = int(1e9)
        result = [[INF] * len(mat[0]) for _ in range(len(mat))]

        queue = deque([(0, 0)])
        visited = set()
        while queue:
            x, y = queue.popleft()
            if (x, y) in visited:
                continue
            visited.add((x, y))
            num = mat[x][y]
            if num == 0:
                result[x][y] = 0
            else:
                if x - 1 >= 0:
                    result[x][y] = min(result[x][y], result[x - 1][y] + 1)
                if y - 1 >= 0:
                    result[x][y] = min(result[x][y], result[x][y - 1] + 1)
            if x + 1 < len(mat):
                queue.append((x + 1, y))
            if y + 1 < len(mat[0]):
                queue.append((x, y + 1))

        queue = deque([(len(mat) - 1, len(mat[0]) - 1)])
        visited = set()
        while queue:
            x, y = queue.popleft()
            if (x, y) in visited:
                continue
            visited.add((x, y))
            num = mat[x][y]
            if num == 0:
                result[x][y] = 0
            else:
                if x + 1 < len(mat):
                    result[x][y] = min(result[x][y], result[x + 1][y] + 1)
                if y + 1 < len(mat[0]):
                    result[x][y] = min(result[x][y], result[x][y + 1] + 1)
            if x - 1 >= 0:
                queue.append((x - 1, y))
            if y - 1 >= 0:
                queue.append((x, y - 1))

        return result


